    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked

        //display the selected row data in the jtextfield

        //get the jtable model
        DefaultTableModel model = (DefaultTableModel)jTable1.getModel();

        //get the selected row index
        int rIndex= jTable1.getSelectedRow();

        //display data
        id.setText(model.getValueAt(rIndex, 0).toString());
        type.setText(model.getValueAt(rIndex, 1).toString());
        jComboBox1.setSelectedItem(model.getValueAt(rIndex, 2).toString());
        material.setText(model.getValueAt(rIndex, 3).toString());
       jComboBox2.setSelectedItem(model.getValueAt(rIndex, 4).toString());
        quan.setText(model.getValueAt(rIndex, 5).toString());
        quan1.setText(model.getValueAt(rIndex, 6).toString());
    }//GEN-LAST:event_jTable1MouseClicked

