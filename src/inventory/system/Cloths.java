    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String Type = type.getText();
        String Material = material.getText();
        String Color =  (String) jComboBox1.getSelectedItem();
        String Sizes =(String) jComboBox2.getSelectedItem();
       int Quantity =  Integer.valueOf(quan.getText());
       String Description= quan1.getText();
        if (Type.trim().equals("") || Material.trim().equals("") || Quantity<0  ){

            JOptionPane.showMessageDialog(rootPane, "Please Fill the Required Fields", "Empty Fields", JOptionPane.WARNING_MESSAGE);
        }else{
            if(mn.addcloths(Type,Color, Material,Sizes, Quantity,Description)){
                JOptionPane.showMessageDialog(rootPane, "New Fabric Added Succesfully", "Add Fabric", JOptionPane.INFORMATION_MESSAGE);

            }else{

                JOptionPane.showMessageDialog(rootPane, "Error while Adding Item", "Item Error", JOptionPane.ERROR_MESSAGE);
            }

        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        //delete the selected client
        try{
            int Id = Integer.valueOf(id.getText());

            if(mn.deletecloths(Id)){
                JOptionPane.showMessageDialog(rootPane, "Item Deleted Succesfully", "Delete Item", JOptionPane.INFORMATION_MESSAGE);

            }else{

                JOptionPane.showMessageDialog(rootPane, "Error while Deleting Item", "Delete Item Error", JOptionPane.ERROR_MESSAGE);
            }
        }       catch(NumberFormatException ex){
            JOptionPane.showMessageDialog(rootPane, ex.getMessage()+" - Enter item Id ", "Item ID Error ", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

