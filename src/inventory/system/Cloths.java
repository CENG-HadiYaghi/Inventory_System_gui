    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        //edit the selected client
        int Id=0;
        String Type = type.getText();
        String Material = material.getText();
        String Color =  (String) jComboBox1.getSelectedItem();
        String Sizes =(String) jComboBox2.getSelectedItem();
        int Quantity = Integer.valueOf(quan.getText());
        String Description= quan1.getText();

        if (Type.trim().equals("") || Quantity<0 || Material.trim().equals("")   ){

            JOptionPane.showMessageDialog(rootPane, "Please Fill the Required Fields", "Empty Fields", JOptionPane.WARNING_MESSAGE);
        }else{
            try{
                Id = Integer.valueOf(id.getText());
                if(mn.editcloths(Type,Color, Material,Sizes, Quantity,Description,Id)){
                    JOptionPane.showMessageDialog(rootPane, "Client Data Updated Succesfully", "Update Client", JOptionPane.INFORMATION_MESSAGE);

                }else{

                    JOptionPane.showMessageDialog(rootPane, "Error while Updating Client", "Edit Client Error", JOptionPane.ERROR_MESSAGE);
                }
            }
            catch(NumberFormatException ex){
                JOptionPane.showMessageDialog(rootPane, ex.getMessage()+" - Enter Client Id ", "Client ID Error ", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_jButton3ActionPerformed

